package ClassExample;

public class DataTypesExample {
    public static void main(String [] args){
        //numeric data types
        byte myFirstByteVariable = 3;
        short myFirstShortVariable = 5;
        int myFistIntVariable = 6;
        long myFirstLongVariable = 7l;
        float myFirstFloatVariable = 8f;
        double myFirstDoubleVariable = 9d;
        // conditions
        boolean myFirstBooleanVariable = false;
        boolean mySecondBooleanVariable = true;

        //Characters data type
        char myFirstCharVariable = 'c';

        //Strings data type
        String myFirstStringVAriable = "Sda Shkoder";

        System.out.println(myFirstByteVariable);
        System.out.println(myFirstShortVariable);
        System.out.println(myFistIntVariable);
        System.out.println(myFirstLongVariable);
        System.out.println(myFirstFloatVariable);
        System.out.println(myFirstDoubleVariable);
        System.out.println(myFirstBooleanVariable);
        System.out.println(mySecondBooleanVariable);
        System.out.println(myFirstCharVariable);
        System.out.println(myFirstStringVAriable);

    }




}


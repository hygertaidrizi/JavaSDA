package ClassExample;

public class Operator3Example {
    public static void main(String args[]){
        //Operatoret e matematikes me numra
        int number_1 = 10;
        int number_2 = 20;

        //Krijojme nje variable tjt qe do jete shuma
        int shuma = number_1 + number_2;
        System.out.println(shuma);

        int zbritja = number_1 - number_2;
        System.out.println(zbritja);

        int shumezimi = number_1 * number_2;
        System.out.println(shumezimi);

        float pjestimi = number_1 / number_2;
        System.out.println(pjestimi);

        int perpjestimi = number_1 % number_2;
        System.out.println(perpjestimi);


        System.out.println("Me poshte do te gjeni veprimet me numra me presje :");
        float numer_3 = 80.0f;
        float number_4 = 20.8098f;

        float shumaFloat = numer_3 + number_4;
        System.out.println(shumaFloat);

        float zbritjaFloat = numer_3 - number_4;
        System.out.println(zbritjaFloat);

        float shumezimeFloat = numer_3 * number_4;
        System.out.println(shumezimeFloat);

        float pjestimiFloat = numer_3 / number_4;
        System.out.println(pjestimiFloat);

        float perpjestimiFloat =  numer_3 % number_4;
        System.out.println(perpjestimiFloat);
        double numer_5 = 80.9;
        double number_6 = 20d;
    }
}

